public class DriverDoc {


    String doc1, doc2, doc3, doc4;

    public DriverDoc(String doc1, String doc2, String doc3, String doc4) {
        this.doc1 = doc1;
        this.doc2 = doc2;
        this.doc3 = doc3;
        this.doc4 = doc4;
    }

    public String getDoc1() {

        return doc1;
    }

    public void setDoc1(String doc1) {
        this.doc1 = doc1;
    }

    public String getDoc2() {
        return doc2;
    }

    public void setDoc2(String doc2) {
        this.doc2 = doc2;
    }

    public String getDoc3() {
        return doc3;
    }

    public void setDoc3(String doc3) {
        this.doc3 = doc3;
    }

    public String getDoc4() {
        return doc4;
    }

    public void setDoc4(String doc4) {
        this.doc4 = doc4;
    }
}
